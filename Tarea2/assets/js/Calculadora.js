class Calculadora {

    sumar(num1, num2) {

        return Number(num1) + Number(num2);
    }

    restar(num1, num2) {

        return Number(num1) - Number(num2);
    }

    multiplicar(num1, num2) {

        return Number(num1) * Number(num2);
    }

    dividir(num1, num2) {

        return Number(num1) / Number(num2);
    }



}